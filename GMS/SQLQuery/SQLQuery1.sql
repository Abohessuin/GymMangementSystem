create database gym
use gym 
create table gymtable (
firstname varchar(30),
startdate varchar(150) not null ,
enddate varchar(150) not null ,
mobile varchar(30),
price varchar(30)
);


create table deletegymtable (
firstname varchar(30),
startdate varchar(150) not null ,
enddate varchar(150) not null ,
mobile varchar(30),
price varchar(30)
);

